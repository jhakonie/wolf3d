#include "wc_draw.h"

void	wc_depth_buffer_set(t_depth_buffer *db, t_f32 x, t_f32 y, t_f32 d)
{
	t_u64	i;

	i = (t_u32)y * db->width + (t_u32)x;
	db->data[i] = d;
}
