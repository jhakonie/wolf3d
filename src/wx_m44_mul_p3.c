#include "wx_math.h"

t_p3	wx_m44_mul_p3(t_m44 const *l, t_p3 const *r)
{
	t_p3	p;

	p.x = l->xs[0] * r->x + l->xs[4] * r->y + l->xs[8] * r->z + l->xs[12];
	p.y = l->xs[1] * r->x + l->xs[5] * r->y + l->xs[9] * r->z + l->xs[13];
	p.z = l->xs[2] * r->x + l->xs[6] * r->y + l->xs[10] * r->z + l->xs[14];
	return (p);
}
