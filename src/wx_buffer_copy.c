#include "wx_types.h"

void	wx_buffer_copy(void *xs, void const *ys, t_u64 size)
{
	t_u64	i;

	i = 0;
	while (i < size)
	{
		((t_u8 *)xs)[i] = ((t_u8 *)ys)[i];
		++i;
	}
}
